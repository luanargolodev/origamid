// Operadores Lógicos &&

true && true; // true
true && false; // false
false && true; // false
"Gato" && "Cão"; // 'Cão'
5 - 5 && 5 + 5; // 0
"Gato" && false; // false
5 >= 5 && 3 < 6; // true

// Se ambos os valores forem true, ele irá retornar o último valor verificado
// Se algum valor for false, ele irá retornar o mesmo e não irá continuar a verificar os próximos
